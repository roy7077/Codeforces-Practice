#include <iostream>
using namespace std;
#define ll long long

int main() 
{
	int t;
	cin>>t;
	while(t--)
	{
	    ll x;
	    cin>>x;
	    
	    cout<<"1 "<<x-1<<endl;
	}
	return 0;
}

